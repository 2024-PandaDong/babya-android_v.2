fun getRegionByCode(code: String): String {
    return when (code.substring(1, 3)) {
        "01" -> "서울"
        "02" -> "경기"
        "03" -> "광주"
        "04" -> "대구"
        "05" -> "대전"
        "06" -> "부산"
        "07" -> "울산"
        "08" -> "인천"
        "09" -> "강원"
        "10" -> "경남"
        "11" -> "경북"
        "12" -> "전남"
        "13" -> "전북"
        "14" -> "충북"
        "15" -> "충남"
        "16" -> "제주"
        "17" -> "전국"
        "18" -> "세종특별자치시"
        else -> "알 수 없음"
    }
}